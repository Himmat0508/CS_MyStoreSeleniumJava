package pyramid_pattern;

public class Program3 {

	public static void main(String[] args) {


		for( int i=5;i>=1 ;i--) {

			for ( int j=i; j>=1;j--) {
				System.out.print(j +" ");

			}
			System.out.println();
		}
		for( int i=1;i<=7 ;i++) {

			for ( int j=i; j>=1;j--) {
				System.out.print(j+1 +" ");

			}
			System.out.println();
		}
	}
	}
